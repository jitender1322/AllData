const mongoose = require("mongoose");

const productSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

productSchema.index({ name: 1, price: -1 });

module.exports = mongoose.model("Product", productSchema);
